# ── Search JS ──────────────────────────────────────────────────────────────────

def _search_js(lang: str) -> str:
    searching   = _ht("searching",    lang).replace("'", "\\'")
    no_results  = _ht("no_results",   lang).replace("'", "\\'")
    failed      = _ht("search_failed",lang).replace("'", "\\'")
    full_report = _ht("full_report",  lang).replace("'", "\\'")
    return f"""
<script>
(function(){{
  var inp = document.getElementById('hp-search');
  var btn = document.getElementById('hp-btn');
  var res = document.getElementById('search-results');
  var st  = document.getElementById('search-status');
  var timer;
  function doSearch(q){{
    if(!q||q.length<2){{res.style.display='none';st.style.display='none';return;}}
    st.textContent='{searching}';
    st.style.display='block';
    res.innerHTML='';
    res.style.display='block';
    clearTimeout(timer);
    timer=setTimeout(function(){{
      fetch('/api/search?q='+encodeURIComponent(q)+'&limit=8')
        .then(function(r){{return r.json();}})
        .then(function(d){{
          st.style.display='none';
          if(!d.results||!d.results.length){{res.innerHTML='<p style="color:#888;padding:16px">{no_results}</p>';return;}}
          res.innerHTML=d.results.map(function(x){{
            var score=x.trust_score?Math.round(x.trust_score):'—';
            var url=x.source_url||x.url||'#';
            return '<div class="result-item"><div><div class="result-name">'+x.name+'</div><div class="result-type">'+(x.type||'')+'</div><a class="result-link" href="'+url+'">{full_report}</a></div><div class="result-score">'+score+'</div></div>';
          }}).join('');
        }})
        .catch(function(){{st.textContent='{failed}';st.style.display='block';}});
    }},350);
  }}
  inp.addEventListener('input',function(){{doSearch(this.value.trim());}});
  btn.addEventListener('click',function(){{doSearch(inp.value.trim());}});
  inp.addEventListener('keydown',function(e){{if(e.key==='Enter')doSearch(this.value.trim());}});
}})();
</script>"""


